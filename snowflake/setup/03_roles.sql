GRANT SELECT ON FUTURE TABLES IN SCHEMA NEXUS_APP.AI         TO ROLE NEXUS_VIEWER;
GRANT SELECT ON FUTURE TABLES IN SCHEMA NEXUS_APP.CORE       TO ROLE NEXUS_ANALYST;

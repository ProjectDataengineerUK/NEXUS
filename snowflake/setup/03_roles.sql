-- NEXUS AI DataOps — RBAC
-- Executar como SECURITYADMIN

USE ROLE SECURITYADMIN;

-- Roles funcionais do produto
CREATE ROLE IF NOT EXISTS NEXUS_ADMIN
    COMMENT = 'Administrador do NEXUS: acesso total, sem masking de PII';

CREATE ROLE IF NOT EXISTS NEXUS_ANALYST
    COMMENT = 'Analista: acesso a marts e agentes, PII mascarado';

CREATE ROLE IF NOT EXISTS NEXUS_VIEWER
    COMMENT = 'Visualizador: dashboards e chat, sem dados brutos';

CREATE ROLE IF NOT EXISTS NEXUS_DATA_ENGINEER
    COMMENT = 'Engenheiro de dados: acesso a RAW, STD, pipelines, quality';

-- Hierarquia de roles
GRANT ROLE NEXUS_VIEWER    TO ROLE NEXUS_ANALYST;
GRANT ROLE NEXUS_ANALYST   TO ROLE NEXUS_ADMIN;
GRANT ROLE NEXUS_ADMIN     TO ROLE SYSADMIN;

-- Grants de warehouse por role
USE ROLE SYSADMIN;

GRANT USAGE ON WAREHOUSE NEXUS_UI_WH          TO ROLE NEXUS_VIEWER;
GRANT USAGE ON WAREHOUSE NEXUS_COMPUTE_WH     TO ROLE NEXUS_ANALYST;
GRANT USAGE ON WAREHOUSE NEXUS_INGEST_WH      TO ROLE NEXUS_DATA_ENGINEER;
GRANT USAGE ON WAREHOUSE NEXUS_ML_WH          TO ROLE NEXUS_ADMIN;
GRANT USAGE ON WAREHOUSE NEXUS_ORCHESTRATION_WH TO ROLE NEXUS_DATA_ENGINEER;

-- Grants de database
GRANT USAGE ON DATABASE NEXUS_APP TO ROLE NEXUS_VIEWER;

-- Grants por schema (VIEWER: apenas MART, AI, CONFIG)
GRANT USAGE ON SCHEMA NEXUS_APP.MART       TO ROLE NEXUS_VIEWER;
GRANT USAGE ON SCHEMA NEXUS_APP.AI         TO ROLE NEXUS_VIEWER;
GRANT USAGE ON SCHEMA NEXUS_APP.CONFIG     TO ROLE NEXUS_VIEWER;

-- ANALYST: adiciona CORE e AUDIT (leitura)
GRANT USAGE ON SCHEMA NEXUS_APP.CORE       TO ROLE NEXUS_ANALYST;
GRANT USAGE ON SCHEMA NEXUS_APP.AUDIT      TO ROLE NEXUS_ANALYST;
GRANT USAGE ON SCHEMA NEXUS_APP.GOVERNANCE TO ROLE NEXUS_ANALYST;

-- DATA_ENGINEER: adiciona RAW e STD
GRANT USAGE ON SCHEMA NEXUS_APP.RAW        TO ROLE NEXUS_DATA_ENGINEER;
GRANT USAGE ON SCHEMA NEXUS_APP.STD        TO ROLE NEXUS_DATA_ENGINEER;

-- ADMIN: acesso total
GRANT ALL ON DATABASE NEXUS_APP            TO ROLE NEXUS_ADMIN;


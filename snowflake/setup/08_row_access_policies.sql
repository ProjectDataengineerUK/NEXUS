ALTER TABLE NEXUS_APP.CORE.DOCUMENTS     ADD ROW ACCESS POLICY NEXUS_APP.GOVERNANCE.ORG_ISOLATION ON (org_id);
ALTER TABLE NEXUS_APP.CORE.INTERACTIONS  ADD ROW ACCESS POLICY NEXUS_APP.GOVERNANCE.ORG_ISOLATION ON (org_id);
ALTER TABLE NEXUS_APP.AI.CHURN_SCORES    ADD ROW ACCESS POLICY NEXUS_APP.GOVERNANCE.ORG_ISOLATION ON (org_id);
ALTER TABLE NEXUS_APP.AI.RECOMMENDATIONS ADD ROW ACCESS POLICY NEXUS_APP.GOVERNANCE.ORG_ISOLATION ON (org_id);
